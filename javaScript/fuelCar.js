function navigator (pageName){
    if(pageName == 'audi'){
        window.location.href = "../html/audi.html"
    }
    else if(pageName == 'bmw'){
        window.location.href = "../html/bmw.html"
    }
    else if(pageName == 'mercedes'){
        window.location.href = "../html/mercedes.html"
    }
    else if(pageName == 'volks'){
        window.location.href = "../html/volks.html"
    }
}