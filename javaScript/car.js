function navigator(pageName){
    if(pageName == 'fuel'){
        window.location.href = "../html/FuelCars.html"
    }
    else if(pageName == 'electric'){
        window.location.href = "../html/Electric Cars.html"
    }
    else if(pageName == 'motorcycles'){
        window.location.href = "../html/MOTORCYCLES.html"
    }
}